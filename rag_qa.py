if __name__ == "__main__":
    ap = argparse.ArgumentParser(
        description="Evaluate a RAG pipeline against gt_qa.json questions.",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    ap.add_argument("--gt",          default=GT_QA_PATH,  help="Path to gt_qa.json")
    ap.add_argument("--chunks-dir",  default=CHUNKS_DIR,  help="Folder with chunk JSONs")
    ap.add_argument("--output",      default=OUTPUT_PATH, help="Output JSON path")
    ap.add_argument("--limit",       type=int, default=None,
                    help="Process only the first N questions (for smoke-testing)")
    ap.add_argument("--dry-run",     action="store_true",
                    help="Run retrieval but skip LLM calls")
    args = ap.parse_args()

    # Guard rails
    if not CHATAI_API_KEY:
        raise SystemExit(
            "ERROR: CHATAI_API_KEY is not set.\n"
            "Add it to your .env:  CHATAI_API_KEY=<your key>"
        )
    if QA_LLM_PROVIDER.lower() == "anthropic" and not ANTHROPIC_API_KEY:
        raise SystemExit(
            "ERROR: QA_LLM_PROVIDER=anthropic but ANTHROPIC_API_KEY is not set."
        )

    run(
        gt_path     = args.gt,
        chunks_dir  = args.chunks_dir,
        output_path = args.output,
        dry_run     = args.dry_run,
        limit       = args.limit,
    )
